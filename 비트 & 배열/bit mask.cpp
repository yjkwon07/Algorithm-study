#include <iostream>

using namespace std;

// 2진수 형태로 출력하기
void show(int *a) {
	for(int i = 32; i > 0; i--) {
		cout << (( *a & ( 1 << i - 1))? 1 : 0);
	}
} 

// 모든 소를 삭제하기 
void init(int * a) {
	*a = 0;
} 

// 모든 원소를 포함시키기
void full(int *a) {
	*a = -1;
} 

// 특정 원소를 삭제하기 
void drop(int *a, int i) {
	*a &= ~(1<<i);
} 

// 특정 원소 포함시키기
void set(int *a, int i) {
	*a |= (1<<i);
} 

// 특정 원소 포함 여부 확인하기 
bool isSet(int *a, int i) {
	return *a & (1<<i);
}

// 특정 원소를 토글하기
void toggle(int *a, int i) {
	*a ^= (1<<i);
} 

// 마지막 원소 구하기
int getLast(int *a) {
	return (*a & -*a);
} 

// 마지막 원소 삭제하기
void dropLast(int *a) {
	*a &= (*a - 1);
} 

int main(void) {
	int a;
	
	init(&a);
	cout << "모든 원소 삭제";
	show(&a);
	cout << '\n'; 

	full(&a);
	cout << "모든 원소 포함";
	show(&a);
	cout << '\n';
	
	drop(&a, 5);
	cout << "5번째 인덱스 삭제: ";
	show(&a);
	cout << '\n';
	
	set(&a, 5); 
	cout << "5번째 인덱스 포함: ";
	show(&a);
	cout << '\n';
	
	cout << "5번째 인덱스 포함 여부: " << isSet(&a, 5);
	cout << '\n';
	
	toggle(&a, 5);
	cout << "5번째 인덱스 토글: ";
	show(&a);
	cout << '\n';
	
	cout << "5번째 인덱스 포함 여부: " << isSet(&a, 5);
	cout << '\n';
	
	dropLast(&a); 
	dropLast(&a);
	dropLast(&a);
	cout << "마지막 원소 3개 삭제: ";
	show(&a);
	cout << '\n';
	
	cout << "마지막 원소 구하기: " << getLast(&a);
	cout << '\n';
}

